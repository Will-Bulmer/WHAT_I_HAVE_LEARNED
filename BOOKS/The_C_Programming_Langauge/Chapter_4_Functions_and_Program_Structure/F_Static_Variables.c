/*---------------------------  4.6 Static Variables -----------------------------------------*/


/*
- The STATIC declaration, applied to an external variable or function, limits the scope of that object to the rest of the source
   file being compiled.
- EXTERNAL STATIC provides a way to hide names. Can be shared but are NOT visible to users.
- If a function is declared STATIC, its name is invisible to outside of the file it was created.
- An INTERNAL STATIC provides private, permanent storage within a single function. 
    unlike automatic, they remain in existence rather than coming and going each time the function is created.
*/