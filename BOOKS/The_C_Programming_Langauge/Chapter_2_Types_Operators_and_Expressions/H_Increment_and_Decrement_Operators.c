/*---------------------------  2.8 Increment and Decrement Operators  -----------------------------------------*/

/*
++              : increment, adds 1
--              : decrement, subtracts 1

n++             : Increments n after its value is used
++n             : Increments n before its value is used

Ie. If n is 5:
    x = n++;    // Sets x to 5
    x = ++n;    // Sets x to 6

    (i+j)++     // Illegal
*/