/*---------------------------  2.2 Data Types and Sizes  -----------------------------------------*/

/*
char            : a single byte. Holds one character in a local character set.
int             : an integer, typically reflecting the natural size of ints on the host machine
float           : single-precision floating point
double          : double-precision floating point
short int       :  ... often 16 bits
long int        :  ... often 32 bits
unsigned        :  ... always positive or zero. Obey laws of arithmetic modulo 2^n. n number of bits in the type.
signed              8 bits signed  : -128 and 127
                    8 bit unsigned : 0 and 255 
*/