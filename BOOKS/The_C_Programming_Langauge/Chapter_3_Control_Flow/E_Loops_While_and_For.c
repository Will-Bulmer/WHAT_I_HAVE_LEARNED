/*---------------------------  3.5 Loops - While and For  -----------------------------------------*/

/*

- expr1, expr3 are function calls
- expr2 is a relational expression
- expr2 omitted is taken as infinitely TRUE
- For loops are NOT restricted to arithmetic progressions

for (expr1 ; exrp2 ; expr3){
    statement
}

- Equivalent to..

expr1;
while(expr2){
    statment
    expr3;
}

*/